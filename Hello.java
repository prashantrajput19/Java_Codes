public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Hey i am using java for coding purpose");
        System.out.println(3+9);
        int num1=23;
        int num2=54;
        System.out.println(num1+num2);
        String firstName="Prashant";
        String lastName="Rajput";
        System.out.println(firstName+" "+lastName);

        // Lets create some variable with different data type 

        // Integer Data type 

        int a=5;
        byte b=2;
        short c=3;
        long d=1234565432;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

        // Float Data type 

        float e=12.3f;
        double f=45.9;
        System.out.println(e);
        System.out.println(f);

        // Char data type 

        char g='a';
        System.out.println(g);

        // Boolean data type 
        boolean h=true;
        System.out.println(h);


        // Literals in java 

        // Binary Number
        int lit=0b111;
        System.out.println(lit);

        // HexaDecimal Number

        int hex=0x7e;
        System.out.println(hex);

        // Best way to count no of zeroes in java 

        int   countZero=1_00_00_00_000;
        System.out.println(countZero);
    }
}
